package com.mmzb.house.app.entity;

/**
 * 版本控制表
 * @author whl
 *
 */
public class AppVersionEntity {

	private Integer id;
	/** 产品类型：android、android 客户端；h5： ios：ios客户端*/
	private String product_type;
	/** 来源：financecollege、金融学院；mmsfang、妈妈送房；wallet、妈妈钱包*/
	private String source;
	/** 版本号*/
	private String version_no;
	/** 版本下载URL*/
	private String url;
	/** 本版本更新类容*/
	private String updateContent;
	/** 更新原因*/
	private String reasoan;
	/** 状态：1、生效；0、无效；*/
	private int versionStatus;
	/** 是否强制更新：1、强制更新；0、非强制更新；*/
	private int forceUpdateFlag;
	/** 启动页图片*/
	private String startImage;
	/** 创建时间，数据库子的那个更新*/
	private String createTime;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getProduct_type() {
		return product_type;
	}
	public void setProduct_type(String product_type) {
		this.product_type = product_type;
	}
	public String getSource() {
		return source;
	}
	public void setSource(String source) {
		this.source = source;
	}
	public String getVersion_no() {
		return version_no;
	}
	public void setVersion_no(String version_no) {
		this.version_no = version_no;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getUpdateContent() {
		return updateContent;
	}
	public void setUpdateContent(String updateContent) {
		this.updateContent = updateContent;
	}
	public String getReasoan() {
		return reasoan;
	}
	public void setReasoan(String reasoan) {
		this.reasoan = reasoan;
	}
	public int getVersionStatus() {
		return versionStatus;
	}
	public void setVersionStatus(int versionStatus) {
		this.versionStatus = versionStatus;
	}
	public int getForceUpdateFlag() {
		return forceUpdateFlag;
	}
	public void setForceUpdateFlag(int forceUpdateFlag) {
		this.forceUpdateFlag = forceUpdateFlag;
	}
	public String getStartImage() {
		return startImage;
	}
	public void setStartImage(String startImage) {
		this.startImage = startImage;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	
	
}
