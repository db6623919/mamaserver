package com.mmzb.house.app.vo;

import java.util.Date;
import java.util.List;

/**
 * 旅居券分组信息
 * 
 * @author whl
 * 
 */
public class HotelCouponGroupVo {

	private List<Long> hotelCouponIds;// 旅居券ID列表
	private String mmWalletId;// 会员ID
	private String name;// 券名称
	private int devId; // 开发商ID
	private int houseId; // 房源ID
	private boolean inSeason; // 是否旺季
	private int status; // 状态
	private Date expireTime; // 过期时间
	private int count; // 数量
	private String disabledDates;// 不可用日期，json字符串

	public int getDevId() {
		return devId;
	}

	public void setDevId(int devId) {
		this.devId = devId;
	}

	public int getHouseId() {
		return houseId;
	}

	public void setHouseId(int houseId) {
		this.houseId = houseId;
	}

	public boolean isInSeason() {
		return inSeason;
	}

	public void setInSeason(boolean inSeason) {
		this.inSeason = inSeason;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public Date getExpireTime() {
		return expireTime;
	}

	public void setExpireTime(Date expireTime) {
		this.expireTime = expireTime;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDisabledDates() {
		return disabledDates;
	}

	public void setDisabledDates(String disabledDates) {
		this.disabledDates = disabledDates;
	}

	public List<Long> getHotelCouponIds() {
		return hotelCouponIds;
	}

	public void setHotelCouponIds(List<Long> hotelCouponIds) {
		this.hotelCouponIds = hotelCouponIds;
	}

	public String getMmWalletId() {
		return mmWalletId;
	}

	public void setMmWalletId(String mmWalletId) {
		this.mmWalletId = mmWalletId;
	}

}
