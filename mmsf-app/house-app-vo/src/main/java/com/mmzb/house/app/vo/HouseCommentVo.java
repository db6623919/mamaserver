package com.mmzb.house.app.vo;


/**
 * 房源评论信息
 * @author majiafei
 *
 */
public class HouseCommentVo
{
	//总评论数
	private int totalCommentsNum;
	
	//平均评分
	private double averageScore;
	
	//置顶评论
	private String summary;
	
	//房源图片
	private String imgUrl;
	
	//房源名称
	private String houseName;
	
	public int getTotalCommentsNum()
	{
		return totalCommentsNum;
	}

	public void setTotalCommentsNum(int totalCommentsNum)
	{
		this.totalCommentsNum = totalCommentsNum;
	}

	public double getAverageScore()
	{
		return averageScore;
	}

	public void setAverageScore(Double averageScore)
	{
		this.averageScore = averageScore;
	}


	public String getSummary()
	{
		return summary;
	}

	public void setSummary(String summary)
	{
		this.summary = summary;
	}

	public String getImgUrl()
	{
		return imgUrl;
	}

	public void setImgUrl(String imgUrl)
	{
		this.imgUrl = imgUrl;
	}

	public String getHouseName()
	{
		return houseName;
	}

	public void setHouseName(String houseName)
	{
		this.houseName = houseName;
	}
}
