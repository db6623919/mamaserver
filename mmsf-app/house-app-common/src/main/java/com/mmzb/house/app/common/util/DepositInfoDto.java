package com.mmzb.house.app.common.util;

import java.io.Serializable;
import java.math.BigDecimal;

public class DepositInfoDto implements Serializable {
	private BigDecimal cz;
	private BigDecimal tx;
	private BigDecimal tz;
	private BigDecimal sy;
	public BigDecimal getTz() {
		return tz;
	}
	public void setTz(BigDecimal tz) {
		this.tz = tz;
	}
	public BigDecimal getSy() {
		return sy;
	}
	public void setSy(BigDecimal sy) {
		this.sy = sy;
	}
	public BigDecimal getCz() {
		return cz;
	}
	public void setCz(BigDecimal cz) {
		this.cz = cz;
	}
	public BigDecimal getTx() {
		return tx;
	}
	public void setTx(BigDecimal tx) {
		this.tx = tx;
	}
  
}
