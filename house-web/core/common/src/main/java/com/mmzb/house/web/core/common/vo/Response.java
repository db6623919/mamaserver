package com.mmzb.house.web.core.common.vo;

/** 响应实体类 */
public class Response<T> {
	/** 响应编码 */
	private String code;
	
	/** 响应提示信息 */
	private String msg;
	
	/** 响应实体对象 */
	private T data;
 
	public Response() {

	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public T getData() {
		return data;
	}

	public void setData(T data) {
		this.data = data;
	}
} 