package com.mmzb.house.web.model.houses;

/**
 * 推荐搜索房源Vo
 * @author dengbiao
 *
 */
public class RecommondHouseVo {

	private int houseId;
	private int price;
	private int marketPrice;//市场价
	private int specials_stauts=0;//特价状态默认0， 0 普通 1特价 2首页特价
	private String specialTag;
	private String description;//几室几厅描述
	private String discountDescription;//折扣
	private String name;//房源名
	private String imageUrl;//房源图片
	
	
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getImageUrl() {
		return imageUrl;
	}
	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}
	public int getHouseId() {
		return houseId;
	}
	public void setHouseId(int houseId) {
		this.houseId = houseId;
	}

	public int getMarketPrice() {
		return marketPrice;
	}
	public void setMarketPrice(int marketPrice) {
		this.marketPrice = marketPrice;
	}
	public int getSpecials_stauts() {
		return specials_stauts;
	}
	public void setSpecials_stauts(int specials_stauts) {
		this.specials_stauts = specials_stauts;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getSpecialTag() {
		return specialTag;
	}
	public void setSpecialTag(String specialTag) {
		this.specialTag = specialTag;
	}
	public String getDiscountDescription() {
		return discountDescription;
	}
	public void setDiscountDescription(String discountDescription) {
		this.discountDescription = discountDescription;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

	
}