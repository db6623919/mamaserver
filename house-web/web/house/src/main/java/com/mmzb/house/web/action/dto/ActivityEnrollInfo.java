package com.mmzb.house.web.action.dto;

/**
 * 活动报名表
 * @author whl
 *
 */
public class ActivityEnrollInfo {
	  
	private Integer id;
	private String  name;//姓名
	private String  phone;//电话
	private String  weChat;//微信号
	private String  age;   //年龄
	private Integer sex;   //性别 0 男性 1 女性
	private Integer educated;//学历 1高中 2大专 3本科 4硕士 5博士
	private String  enroll_time;//报名时间
	private Integer status;     //确认状态 0 未确认 1确认
	private String confirm_time;//确认时间
	  
		public Integer getId() {
			return id;
		}
		public void setId(Integer id) {
			this.id = id;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public String getPhone() {
			return phone;
		}
		public void setPhone(String phone) {
			this.phone = phone;
		}
		public String getWeChat() {
			return weChat;
		}
		public void setWeChat(String weChat) {
			this.weChat = weChat;
		}
		public String getAge() {
			return age;
		}
		public void setAge(String age) {
			this.age = age;
		}
		public Integer getSex() {
			return sex;
		}
		public void setSex(Integer sex) {
			this.sex = sex;
		}
		public Integer getEducated() {
			return educated;
		}
		public void setEducated(Integer educated) {
			this.educated = educated;
		}
		public String getEnroll_time() {
			return enroll_time;
		}
		public void setEnroll_time(String enroll_time) {
			this.enroll_time = enroll_time;
		}
		public Integer getStatus() {
			return status;
		}
		public void setStatus(Integer status) {
			this.status = status;
		}
		public String getConfirm_time() {
			return confirm_time;
		}
		public void setConfirm_time(String confirm_time) {
			this.confirm_time = confirm_time;
		}
	  
}