package com.console.vo;

public class MemberVO {
	
	private String name;//姓名
	private String phone;//手机号码
	private String nickName;//昵称
	private String idCard;//身份证号码
	private String status;//状态
	private String channel;//渠道
	private String email;//邮箱
	private String uid;//会员ID
	private String cardId;//姓名
	private String totalRechargeAmt;//手机号码
	private String totalRewardAmt;//昵称
	private String availAmt;//身份证号码
	private String freezeAmt;//状态
	private String level;//渠道
	public String getCardId() {
		return cardId;
	}
	public void setCardId(String cardId) {
		this.cardId = cardId;
	}
	public String getTotalRechargeAmt() {
		return totalRechargeAmt;
	}
	public void setTotalRechargeAmt(String totalRechargeAmt) {
		this.totalRechargeAmt = totalRechargeAmt;
	}
	public String getTotalRewardAmt() {
		return totalRewardAmt;
	}
	public void setTotalRewardAmt(String totalRewardAmt) {
		this.totalRewardAmt = totalRewardAmt;
	}
	public String getAvailAmt() {
		return availAmt;
	}
	public void setAvailAmt(String availAmt) {
		this.availAmt = availAmt;
	}
	public String getFreezeAmt() {
		return freezeAmt;
	}
	public void setFreezeAmt(String freezeAmt) {
		this.freezeAmt = freezeAmt;
	}
	public String getLevel() {
		return level;
	}
	public void setLevel(String level) {
		this.level = level;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getNickName() {
		return nickName;
	}
	public void setNickName(String nickName) {
		this.nickName = nickName;
	}
	public String getIdCard() {
		return idCard;
	}
	public void setIdCard(String idCard) {
		this.idCard = idCard;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getChannel() {
		return channel;
	}
	public void setChannel(String channel) {
		this.channel = channel;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	@Override
	public String toString() {
		return "MemberVO [name=" + name + ", phone=" + phone + ", nickName="
				+ nickName + ", idCard=" + idCard + ", status=" + status
				+ ", channel=" + channel + ", email=" + email + ", uid=" + uid
				+ "]";
	}
	
	
}
