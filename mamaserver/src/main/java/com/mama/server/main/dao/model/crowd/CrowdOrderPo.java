package com.mama.server.main.dao.model.crowd;

import java.util.Date;

/**
 * 众筹订单信息表
 * */
public class CrowdOrderPo {
	/** 主键id，自增 */
	private Integer id;
	
	/** 业务订单号："CROWD_" + uid+ "_" + 时间戳 */
	private String buOrderNo;
	
	/** 登录用户id */
	private String uid;
	
	/** 联系人ID */
	private Integer contactUserId;
	
	/** 认筹项目ID */
	private Integer projectId;
	
	/** 众筹收益类型ID */
	private Integer revenueId;
	
	/** 认筹份数 */
	private Integer num;
	
	/** 付费方式:1、微信支付；2、支付宝支付； */
	private Integer payType;
	
	/** 总价格 */
	private Double totalAmt;
	
	/** 实付金额 */
	private Double realAmt;
	
	/** 订单状态：0、待支付；1、待确认；2、支付成功； 3、退款中；4、已退款；5、支付支付 */
	private Integer status;
	
	/** 是否已删除：0、未删除；1、软删除； */
	private Integer isRemoved;
	
	/** 对账时间 */
	private Date checkTime;
	
	/** 对账状态：0、未对账；1、对账成功；2对账失败；3、待确认； */
	private Integer checkStatus;
	
	/** 创建时间 */
	private Date createTime;
	
	/** 修改时间：数据库自动更新 */
	private Date updateTime;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getBuOrderNo() {
		return buOrderNo;
	}

	public void setBuOrderNo(String buOrderNo) {
		this.buOrderNo = buOrderNo;
	}

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

	public Integer getContactUserId() {
		return contactUserId;
	}

	public void setContactUserId(Integer contactUserId) {
		this.contactUserId = contactUserId;
	}

	public Integer getProjectId() {
		return projectId;
	}

	public void setProjectId(Integer projectId) {
		this.projectId = projectId;
	}

	public Integer getRevenueId() {
		return revenueId;
	}

	public void setRevenueId(Integer revenueId) {
		this.revenueId = revenueId;
	}

	public Integer getNum() {
		return num;
	}

	public void setNum(Integer num) {
		this.num = num;
	}

	public Integer getPayType() {
		return payType;
	}

	public void setPayType(Integer payType) {
		this.payType = payType;
	}

	public Double getTotalAmt() {
		return totalAmt;
	}

	public void setTotalAmt(Double totalAmt) {
		this.totalAmt = totalAmt;
	}

	public Double getRealAmt() {
		return realAmt;
	}

	public void setRealAmt(Double realAmt) {
		this.realAmt = realAmt;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public Integer getIsRemoved() {
		return isRemoved;
	}

	public void setIsRemoved(Integer isRemoved) {
		this.isRemoved = isRemoved;
	}

	public Date getCheckTime() {
		return checkTime;
	}

	public void setCheckTime(Date checkTime) {
		this.checkTime = checkTime;
	}

	public Integer getCheckStatus() {
		return checkStatus;
	}

	public void setCheckStatus(Integer checkStatus) {
		this.checkStatus = checkStatus;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public Date getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}
}
